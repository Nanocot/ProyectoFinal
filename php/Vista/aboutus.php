    <h1>FARKO</h1>

    <p>FARKO nace de las calles de Tarancón, en agosto de 2023, pero la historia empezó mucho antes.</p>

    <p>Dos colegas, cuatro años de curro y aprendizaje en el mundo del textil, decidimos dar forma a nuestra visión: una marca de ropa que va más allá del postureo. Queríamos contar historias, expresar lo que vivimos, lo que sentimos, lo que somos.</p>

    <p>Nuestro taller, situado en nuestro propio pueblo, es donde pasa la magia. Allí estampamos a mano cada prenda mediante serigrafía artesanal, desde camisetas hasta sudaderas, pasando por nuestras propias etiquetas. Todo lo hacemos nosotros, sin intermediarios, sin filtros. Solo tinta, tela y corazón.</p>

    <p>FARKO tiene raíces underground, influencias de la música urbana y mucho de lo que se vive en la calle. Cada diseño nace con intención, con mensaje. Queremos que nuestras prendas no solo se lleven, sino que te representen, que hablen por ti, que motiven o despierten algo en quien las vea.

Aquí no vas a encontrar fast fashion. Aquí hay dureza, realidad, estilo y familia.</p>
